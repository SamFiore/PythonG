#Tipo de dato entero (int) o largo (long)
numero = 15
#type(numero) - saber tipo de variable
print (numero, type (numero))

print ("----------")

#Tipo de dato flotante (float)
numero_flotante = 15.5
print (numero_flotante, type (numero_flotante))

print ("----------")

#Tipo de dato NÃºmero complejo (complex)
numero_complejo = 5 + 6j
print (numero_complejo, type (numero_complejo))

print ("----------")

#Tipo de dato String("/')
nombre = "Mundo"
print (nombre, type(nombre))

print ("----------")

#Tipo de dato booleano (bool)
verdadero_falso = 3 == 3
print (verdadero_falso, type (verdadero_falso))




